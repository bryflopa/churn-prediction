"""###Se importan las librerias necesarias"""

from sklearn.preprocessing import OneHotEncoder, LabelEncoder, StandardScaler
from sklearn.model_selection import train_test_split, GridSearchCV
from imblearn.pipeline import Pipeline
from imblearn.under_sampling import RandomUnderSampler
from imblearn.over_sampling import SMOTE
from sklearn.linear_model import LogisticRegression, LinearRegression
from sklearn.compose import ColumnTransformer
from sklearn.metrics import accuracy_score, precision_score, f1_score, confusion_matrix, recall_score
from sklearn.ensemble import RandomForestClassifier

"""###Se descarga el dataset de KAGGLE"""

import kagglehub
import os

# Download latest version
path = kagglehub.dataset_download("abbas829/bank-customer-churn")

print("Path to dataset files:", path)


print(os.listdir(path))

archivo = pd.read_csv(path + '/Bank_Churn.csv')
archivo.head()

"""Validamos los valores faltantes"""

archivo.isnull().sum()

x = archivo.drop(columns= {'CustomerId','HasCrCard','Exited'},axis=1)
y = archivo['Exited']

var_num = x.select_dtypes(include = {'int64','float64'}).columns
var_cat = x.select_dtypes(include = {'object'}).columns

"""Se validan la correlacion de las variables:
1 o -1 indican que tienen una correlacion fuerte... lejanos significa que no tienen nada que ver

###Se valida si esta desbalanceado el DataSet
"""

archivo['Exited'].value_counts()

"""###Se crea Columntransformer para tratar los datos antes del Pipeline"""

columntransformer = ColumnTransformer(transformers=[
    ("variables_numericas",StandardScaler(),var_num),
    ("variables_categoricas",OneHotEncoder(handle_unknown='ignore'),var_cat)
])

"""###SE REALIZA COMO PRIMERA OPCION CON REGRESION LOGISTICA"""

pipeline = Pipeline([
    ('preprocess',columntransformer),
    ('balanceo',SMOTE(random_state=42)),
    ('Logistic_regression',LogisticRegression(class_weight="balanced"))
])

pipeline_RandomForest = Pipeline([
    ('preprocess',columntransformer),
    ('balanceo',SMOTE(random_state=42)),
    ('RandomForest',RandomForestClassifier())
])

"""###Se entrena con TRAIN_TEST_SPLIT como primer metodo"""

X_train,X_test,y_train,y_test = train_test_split(x,y,test_size=0.2,random_state = 42)
print(X_train.shape, y_train.shape)
print(X_test.shape, y_test.shape)

"""###SE ENTRENA EL PIPELINE"""

pipeline.fit(X_train,y_train)

pipeline_RandomForest.fit(X_train,y_train)

"""###Se realizan las predicciones y la matriz de confusion"""

y_pred = pipeline.predict(X_test)
print(y_pred)

y_pred = pipeline_RandomForest.predict(X_test)
print(y_pred)

"""####Matriz Confusion RandomForest"""

param_grid = {
    'RandomForest__n_estimators': [100, 200, 300],
    'RandomForest__max_depth': [None, 10, 20],
    'RandomForest__min_samples_split': [2, 5, 10]
}

grid_search = GridSearchCV(estimator = pipeline_RandomForest, cv = 5, param_grid = param_grid,scoring = 'f1', n_jobs = -1)
grid_search.fit(X_train,y_train)
print("Mejores parametros: ", grid_search.best_params_)
print(grid_search.best_score_)

print("Matriz de confusion RandomForest: ", confusion_matrix(y_pred, y_test))

"""####Matriz Confusion Regresion Logistica"""

print("Matriz de confusion Logistic_Regression: ", confusion_matrix(y_pred, y_test))